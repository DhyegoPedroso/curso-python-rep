# Tipo de dado boll (bollean)
# Ao questionar algo em um programa,
# só existem duas respostas possiveis:
# sim (True) ou não (False).
# Existem varios operadores para "questionar".
# Dentre eles o ==, que é um operador logico que
# questiona se um valor e igual a outro.
print(10 == 10)
print(10 != 10)
print(10 == 11)
print(10 != 11)
print(type(10 != 11))